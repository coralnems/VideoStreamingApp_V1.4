package com.example.item;

public class ItemSeason {
    private String seasonId;
    private String seasonName;
    private String seasonTrailer;
    private String seasonPoster;

    public String getSeasonId() {
        return seasonId;
    }

    public void setSeasonId(String seasonId) {
        this.seasonId = seasonId;
    }

    public String getSeasonName() {
        return seasonName;
    }

    public void setSeasonName(String seasonName) {
        this.seasonName = seasonName;
    }

    public String getSeasonTrailer() {
        return seasonTrailer;
    }

    public void setSeasonTrailer(String seasonTrailer) {
        this.seasonTrailer = seasonTrailer;
    }

    public String getSeasonPoster() {
        return seasonPoster;
    }

    public void setSeasonPoster(String seasonPoster) {
        this.seasonPoster = seasonPoster;
    }
}
